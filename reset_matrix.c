void	reset_matrix(int (*matrix)[4])
{
	int row;
	int col;

	row = 0;
	while (row < 4)
	{
		col = 0;
		while (col < 4)
		{
			matrix[row][col] = 0;
			++col;
		}
		++row;
	}
}
